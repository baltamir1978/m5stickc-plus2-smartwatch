// ============================================================================
//  European TV-B-Gone POWER codes (decompressed to raw mark/space microseconds)
// ----------------------------------------------------------------------------
//  Source (transcribed VERBATIM, then decompressed by the same algorithm the
//  original firmware uses):
//    Ken Shirriff / Mitch Altman + Limor Fried "Arduino-TV-B-Gone"
//    https://github.com/shirriff/Arduino-TV-B-Gone
//    file: WORLD_IR_CODES.h  (EUpowerCodes[] table)
//    raw: https://raw.githubusercontent.com/shirriff/Arduino-TV-B-Gone/master/WORLD_IR_CODES.h
//
//  Each entry is the EUpowerCodes[] compressed IrCode expanded into a flat
//  alternating mark/space array in microseconds (times[] units are 10us, so
//  multiplied by 10). Carrier kHz = round(stored freq / 1000). The trailing
//  lead-out/gap of each code is preserved as in the source.
//
//  Non-pulsed (freq==0, unmodulated) EU codes are intentionally OMITTED because
//  IRsend::sendRaw always emits a carrier and cannot faithfully reproduce them.
//
//  Spaces (off-times) longer than 65535us (the sendRaw uint16_t limit) are
//  clamped to 65535. Only inter-frame/lead-out SILENCE is affected; every
//  emitted IR mark (pulse) is the exact source value. (Max mark in this data
//  is 19530us, so no mark is ever clamped.)
// ============================================================================

